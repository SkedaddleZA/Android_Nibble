public class MenuModel {

        String name;
        String suburb;


        public MenuModel(String name, String suburb) {
            this.name=name;
            this.suburb=suburb;

        }

        public String getName() {
            return name;
        }

        public String getSuburb() {
            return suburb;
        }





}
